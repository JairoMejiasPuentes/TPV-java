    private void botonCobrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCobrarActionPerformed


        RegistroVenta rv = new RegistroVenta();
        float precioTotal = Float.parseFloat(this.totalPrecioVenta.getText());
        rv.setPrecioTotal(precioTotal);

        gest.insertarVenta(rv);
        
        VentaProducto vp = new VentaProducto();
        vp.setVendedor(usuarioRegistrado);
;
        for (int i = 0; i < model.size(); i++) {
            String[] parts = String.valueOf(model.get(i)).split(" x");
            vp.setIdVenta(gest.recuperaTicket());
            vp.setIdProducto(gest.recuperaIdProducto(parts[0]));
            vp.setCantidad(Integer.parseInt(parts[1]));
            gest.insertarVP(vp);
        }
        

        model.removeAllElements();
        this.totalIva.setText("0.0");
        this.totalPrecioVenta.setText("0.0");
        numProductos = "";
    }//GEN-LAST:event_botonCobrarActionPerformed

