    /**
     * devuelve el numero de un solo producto pedido en una misma cuenta
     * @return 
     */
    private int recuperaNumProductos(){
        int resultado;
        if(numProductos == "") resultado = 1;
        else resultado = Integer.parseInt(numProductos);
        
        return resultado;
    }
    
    /**
     * Suma el precio del nuevo producto añadido a la cuenta actual
     *
     * @param p Producto que se añade a la cuenta
     */
    private void sumaCuenta(Producto p) {
        String cuentaTxt;       

        Float cuentaActual = Float.parseFloat(this.totalPrecioVenta.getText());
        Float precioProducto = (p.getPrecio() + p.getIva())*recuperaNumProductos();

        cuentaActual = cuentaActual + precioProducto;
        cuentaTxt = String.valueOf(cuentaActual);

        this.totalPrecioVenta.setText(cuentaTxt);
        numProductos = "";

    }

    /**
     * Resta de la cuenta total el producto eliminado del ticket
     *
     * @param p producto que se ha borrado de la lista
     */
    private void restaCuenta(Producto p,int numPedido) {
        String cuentaTxt;
        Float cuentaActual = Float.parseFloat(this.totalPrecioVenta.getText());
        Float precioProducto = (p.getPrecio() + p.getIva()) * numPedido;

        cuentaActual = cuentaActual - precioProducto;
        if (cuentaActual < 0) {
            cuentaActual = (float) 0;
        }

        cuentaTxt = String.valueOf(cuentaActual);

        this.totalPrecioVenta.setText(cuentaTxt);
    }

    /**
     * Calcula el Iva de los productos y lo va añadiendo al ticket
     *
     * @param p Producto al que se le va a calcular el IVA
     * @return devuelve un producto con el iva ya incluido
     */
    private void sumaIva(Producto p) {
        String ivaTxt;
        float ivaActual = Float.parseFloat(this.totalIva.getText());
        float ivaProducto = p.getIva() * recuperaNumProductos();

        ivaActual = ivaActual + ivaProducto;
        ivaTxt = String.valueOf(ivaActual);

        this.totalIva.setText(ivaTxt);

    }
    
