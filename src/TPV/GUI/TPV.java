    private void friosSerranoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_friosSerranoMouseClicked
        int idProducto = Integer.parseInt(this.friosSerrano.getName());
        Producto p = gest.buscarProducto(idProducto);

        if (!compruebaCuenta(p.getNombre())) {
            if(numProductos.equals("")) numProductos = "1";
            cuenta.add(p.getNombre() +" x"+numProductos);
        }
        
        for (int i = 0; i < cuenta.size(); i++) {

            model.addElement(cuenta.get(i));
            cuenta.remove(i);
        }
        sumaIva(p);
        sumaCuenta(p);
        numProductos = "";
    }//GEN-LAST:event_friosSerranoMouseClicked

