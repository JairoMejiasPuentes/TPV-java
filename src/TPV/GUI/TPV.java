    private void botonBorrarUnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarUnoActionPerformed
        try {
            // parts[0] contendra el nombre del producto
            //parts[1] contendrá el numero de los productos especificos pedido
            String[] parts = Ticket.getSelectedValue().split(" x");
            int numPedido = Integer.parseInt(parts[1]);
            
            model.remove(Ticket.getSelectedIndex());

            restaCuenta(gest.buscarProducto(parts[0]),numPedido);
            restaIva(gest.buscarProducto(parts[0]), numPedido);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "selecciona un elemento del ticket");
        }
    }//GEN-LAST:event_botonBorrarUnoActionPerformed

