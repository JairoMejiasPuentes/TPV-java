    private void informeVentasFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_informeVentasFechaActionPerformed
        this.PanelReporte.removeAll();
        inicio = datePickerIni.getJFormattedTextField().getText();
        fin = datePickerFin.getJFormattedTextField().getText();
      
        JasperPrint jp1 = null;
        JRViewer viewer1 = null;
        String ficheroReporteVentasFecha = "reports\\informeVentasFechaNuevo.jrxml";

        HashMap<String, Object> parameterMap = new HashMap<String, Object>();

        Connection conexion;

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            conexion = DriverManager.getConnection("jdbc:mysql://localhost/sandwichdb", "root", "");

            JasperReport reporteCompilado2 = JasperCompileManager.compileReport(ficheroReporteVentasFecha);

            parameterMap.put("fechaInicio", inicio);
            parameterMap.put("fechaFin", fin);

            jp1 = JasperFillManager.fillReport(reporteCompilado2, parameterMap, conexion);

        } catch (ClassNotFoundException | SQLException | JRException ex) {
            System.out.println(ex);
        }
        viewer1 = new JRViewer(jp1);

        this.PanelReporte.add(viewer1);

        this.PanelReporte.revalidate();
    }//GEN-LAST:event_informeVentasFechaActionPerformed

