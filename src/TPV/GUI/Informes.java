    private void informeComprasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_informeComprasActionPerformed
        if (!cantidadStockProducto.getText().equals("")) {
            this.PanelReporte.removeAll();
        JasperPrint jp3 = null;
        JRViewer viewer = null;
        String ficheroReporteCompras = "reports\\Compras.jrxml";

        HashMap<String, Object> parameterMap = new HashMap<String, Object>();

        Connection conexion;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            conexion = DriverManager.getConnection("jdbc:mysql://localhost/sandwichdb", "root", "");

            JasperReport reporteCompilado = JasperCompileManager.compileReport(ficheroReporteCompras);

            parameterMap.put("cantidadStock", Integer.parseInt(cantidadStockProducto.getText()));

            jp3 = JasperFillManager.fillReport(reporteCompilado, parameterMap, conexion);

        } catch (ClassNotFoundException | SQLException | JRException ex) {
            System.out.println(ex);
        }
        viewer = new JRViewer(jp3);

        this.PanelReporte.add(viewer);

        this.PanelReporte.revalidate();
        }else{
            JOptionPane.showMessageDialog(this, "<html><h2>Introduce un valor para el stock "
                    + "</h2><br></html>" , 
                    "Error de verificación", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_informeComprasActionPerformed

